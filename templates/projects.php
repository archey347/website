<h3 style="margin-bottom: 5px;">Ray Tracer</h3>
<i>Autumn 2023</i></p>
<p>
A project for an Adanced Computer Graphics module final year module at University (one of my most favourite modules), taught by <a class="link" href="https://researchportal.bath.ac.uk/en/persons/ken-cameron">Ken Cameron</a>. Features included shadows, reflections, transparency and motion blur (including rolling shutter).
</p>
<p>
We had to submit the code and a report, and the week after a final image that would demonstrate the functions of our ray tracer. I can't share the code
for the project, to stop future students copying, but I can share the final image.
</p>
<p>
Unfortunutly I discovered a memory leak in my ray tracer the week before the final image was due in, which hindered the quality of the final picture I could produce (generating 
it used all 64GB of memory in my laptop!). Otherwise I would've been able to use a greater number of samples for the motion blur, and produced longer 
star streaks.
</p>
<p>
One day I might revisit it to try and hunt out that memory leak.
</p>

