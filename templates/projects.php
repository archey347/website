<h3 style="margin-bottom: 5px;">Diary Generator</h3>
<i>September 2018</i></p>
<p>
My family always have a paper diary to keep track of who is doing what. Unlike a traditional 
calendar, it has a column for each person, and an extra for birthdays.
</p>
<p>
To create this diary, my mum had a set of seven word documents with a pre-filled in table with all the dates for each day of the week. So if 
you had a month that started on a Tuesday, you could select the Tuesday word document, update 
the title and adjust the number of days in the month to suit.
</p>
<p>
The task of printing of a new diary each year often got delegated to me. I found using the above 
system quite repetative, so I ended up coding a solution instead.
</p>
